# ─── Latency benchmark ────────────────────────────────────────────────────────
def benchmark_latency(
    models: Dict[str, Any],
    X_sample: np.ndarray,
    n_repeats: int = 200,
) -> pd.DataFrame:
    """Time single-sample inference for each model.

    Returns a DataFrame with columns [model, mean_ms, std_ms].
    X_sample shape: (1, 18) or (n, 18) — uses first row only.
    """
    x1 = X_sample[:1]
    rows = []
    for name, model in models.items():
        times = []
        for _ in range(n_repeats):
            t0 = time.perf_counter()
            predict(model, x1)
            times.append((time.perf_counter() - t0) * 1000)
        rows.append({
            "Model":    name,
            "Mean (ms)": f"{np.mean(times):.4f}",
            "Std (ms)":  f"{np.std(times):.4f}",
        })
    return pd.DataFrame(rows)


def plot_dnn_training_curves(
    per_seed_meta: List[Dict],
    save_path: Path | None = None,
) -> Path:
    """Val-RMSE vs epoch for each seed of the best DNN architecture.

    per_seed_meta is the list of meta dicts from train_multi_seed, each
    containing 'val_history' (list of per-epoch val RMSE), 'seed', 'hidden',
    'dropout', and 'lr'.
    """
    if save_path is None:
        save_path = EVAL_FIG_DIR / "dnn_training_curves.png"

    fig, ax = plt.subplots(figsize=(7, 4))
    colors = plt.cm.tab10.colors
    for meta, color in zip(per_seed_meta, colors):
        history = meta["val_history"]
        label   = f"seed={meta['seed']}  lr={meta['lr']:.0e}"
        ax.plot(range(1, len(history) + 1), history,
                color=color, lw=1.5, label=label)
        # mark the best epoch
        best_ep = int(np.argmin(history))
        ax.scatter(best_ep + 1, history[best_ep], color=color, s=40, zorder=5)

    arch   = per_seed_meta[0]["hidden"]
    drop   = per_seed_meta[0]["dropout"]
    ax.set_xlabel("Epoch")
    ax.set_ylabel("Validation RMSE (z-score)")
    ax.set_title(
        f"DNN training curves — best arch {list(arch)}, dropout={drop}\n"
        f"Dot = early-stop checkpoint"
    )
    ax.legend(fontsize=8)
    fig.tight_layout()
    fig.savefig(_ensure(save_path))
    plt.close(fig)
    return save_path


def save_latency_table(df: pd.DataFrame, path: Path) -> Path:
    """Write latency DataFrame as a LaTeX booktabs table."""
    _ensure(path)
    lines = [
        r"\begin{tabular}{lrr}", r"\toprule",
        r"Model & Mean (ms) & Std (ms) \\ \midrule",
    ]
    for _, row in df.iterrows():
        lines.append(f'{row["Model"]} & {row["Mean (ms)"]} & {row["Std (ms)"]} \\\\')
    lines += [r"\bottomrule", r"\end{tabular}"]
    path.write_text("\n".join(lines))
    return path

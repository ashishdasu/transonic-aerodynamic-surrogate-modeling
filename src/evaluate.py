"""Evaluation: metrics, figures, and latency benchmark.

Produces (all regenerated by ``make eval``):

  results/tables/
    test_metrics.tex              R², RMSE per (model, target) on test split
    heldout_metrics.tex           same on the RAE2822 unseen-geometry slice
    latency.tex                   mean ± std single-sample inference time (ms)

  results/figures/eval/
    parity_<model>.png            predicted vs true scatter (both targets)
    residuals_mach_<model>.png    RMSE per Mach bin
    cm_vs_alpha.png               Cm(α) overlay: CFD truth + all models
    feature_importance.png        RF and XGBoost normalized importances

All public functions accept raw (unscaled) y_true / y_pred arrays in the
original coefficient space (after inverse-transforming the z-scored preds).
"""
from __future__ import annotations

import time
